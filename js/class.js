class Collection {
    constructor(titulo, precio) {
        this.titulo = titulo;
        this.precio = precio;
    }
}

class Titulo {
    constructor(id, vol, serie, name, author, type, offer, image) {
        this.id = id;
        this.vol = vol;
        this.serie = serie;
        this.name = name;
        this.author = author;
        this.type = type;
        this.offer = offer;
        this.image = image;
    }
}

